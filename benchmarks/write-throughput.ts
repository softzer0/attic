interface BenchmarkConfig {
  readonly sequentialWrites: number;
  readonly concurrentWrites: number;
  readonly concurrency: number;
  readonly bulkRows: number;
  readonly bulkBatchSize: number;
  readonly warmupWrites: number;
}

interface LatencySummary {
  readonly p50Ms: number;
  readonly p95Ms: number;
  readonly p99Ms: number;
  readonly maxMs: number;
}

interface BenchmarkResult extends LatencySummary {
  readonly scenario: string;
  readonly implementation: "PostgreSQL baseline" | "Attic";
  readonly operations: number;
  readonly rows: number;
  readonly durationMs: number;
  readonly operationsPerSecond: number;
  readonly rowsPerSecond: number;
  readonly postCommitSync?: LatencySummary;
  readonly outboxBacklog?: number;
}

class TrackingRedis implements RedisLike {
  public readonly touchedKeys = new Set<string>();

  public constructor(private readonly client: RedisLike) {}

  public get(key: string): Promise<string | null> {
    return this.client.get(key);
  }

  public async set(key: string, value: string, options?: RedisSetOptions): Promise<string | null> {
    this.touchedKeys.add(key);
    return this.client.set(key, value, options);
  }

  public del(key: string): Promise<number> {
    return this.client.del(key);
  }

  public eval(script: string, options?: RedisEvalOptions): Promise<unknown> {
    for (const key of options?.keys ?? []) this.touchedKeys.add(key);
    return this.client.eval(script, options);
  }

  public async cleanup(): Promise<void> {
    await Promise.all([...this.touchedKeys].map((key) => this.client.del(key)));
    this.touchedKeys.clear();
  }
}

class PostCommitSyncTracker {
  private readonly commits = new Map<string, number>();
  private samples: number[] = [];

  public readonly onEvent = (event: AtticEvent): void => {
    const now = performance.now();
    if (event.type === "write.committed") this.commits.set(event.requestId, now);
    if (event.type !== "write.synchronized") return;

    const committedAt = this.commits.get(event.requestId);
    if (committedAt !== undefined) this.samples.push(now - committedAt);
    this.commits.delete(event.requestId);
  };

  public take(): LatencySummary | undefined {
    const result = this.samples.length === 0 ? undefined : summarizeLatencies(this.samples);
    this.commits.clear();
    this.samples = [];
    return result;
  }

  public reset(): void {
    this.commits.clear();
    this.samples = [];
  }
}

function positiveInteger(name: string, fallback: number): number {
  const configured = process.env[name];
  if (configured === undefined) return fallback;
  const value = Number(configured);
  if (!Number.isSafeInteger(value) || value <= 0) throw new TypeError(`${name} must be a positive integer.`);
  return value;
}

function benchmarkConfig(): BenchmarkConfig {
  return {
    sequentialWrites: positiveInteger("ATTIC_BENCH_SEQUENTIAL_WRITES", 100),
    concurrentWrites: positiveInteger("ATTIC_BENCH_CONCURRENT_WRITES", 500),
    concurrency: positiveInteger("ATTIC_BENCH_CONCURRENCY", 16),
    bulkRows: positiveInteger("ATTIC_BENCH_BULK_ROWS", 5_000),
    bulkBatchSize: positiveInteger("ATTIC_BENCH_BULK_BATCH_SIZE", 500),
    warmupWrites: positiveInteger("ATTIC_BENCH_WARMUP_WRITES", 10),
  };
}

function quoteIdentifier(identifier: string): string {
  return `"${identifier.replaceAll('"', '""')}"`;
}

function schemaSql(schema: string): string {
  return `
CREATE SCHEMA ${quoteIdentifier(schema)};
CREATE TABLE ${quoteIdentifier(schema)}.${quoteIdentifier("app_users")} (
  "id" integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  "email" text NOT NULL UNIQUE,
  "name" text
);
`;
}

function benchmarkManifest(schema: string, namespace: string): AtticManifest<PrismaClient> {
  return {
    version: 1,
    sqlAbiVersion: 1,
    namespace,
    schemaChecksum: "b".repeat(64),
    models: {
      User: {
        name: "User",
        dbName: "app_users",
        schema,
        tag: "model:User",
        relations: {},
      },
    },
    implicitJoinTables: [],
    triggers: [
      {
        name: triggerName(schema, "app_users", namespace),
        schema,
        table: "app_users",
        tags: ["model:User"],
      },
    ],
  };
}

function percentile(sorted: readonly number[], ratio: number): number {
  const index = Math.max(0, Math.ceil(sorted.length * ratio) - 1);
  return sorted[index] ?? 0;
}

function rounded(value: number): number {
  return Math.round(value * 100) / 100;
}

function summarizeLatencies(samples: readonly number[]): LatencySummary {
  const sorted = [...samples].sort((left, right) => left - right);
  return {
    p50Ms: rounded(percentile(sorted, 0.5)),
    p95Ms: rounded(percentile(sorted, 0.95)),
    p99Ms: rounded(percentile(sorted, 0.99)),
    maxMs: rounded(sorted.at(-1) ?? 0),
  };
}

async function measure(
  scenario: string,
  implementation: BenchmarkResult["implementation"],
  operations: number,
  rows: number,
  concurrency: number,
  operation: (index: number) => Promise<unknown>,
): Promise<BenchmarkResult> {
  let nextIndex = 0;
  const latencies: number[] = [];
  const startedAt = performance.now();
  const runners = Array.from({ length: Math.min(concurrency, operations) }, async () => {
    while (nextIndex < operations) {
      const index = nextIndex;
      nextIndex += 1;
      const operationStartedAt = performance.now();
      await operation(index);
      latencies.push(performance.now() - operationStartedAt);
    }
  });
  await Promise.all(runners);
  const durationMs = performance.now() - startedAt;

  return {
    scenario,
    implementation,
    operations,
    rows,
    durationMs: rounded(durationMs),
    operationsPerSecond: rounded((operations * 1_000) / durationMs),
    rowsPerSecond: rounded((rows * 1_000) / durationMs),
    ...summarizeLatencies(latencies),
  };
}

function batches(totalRows: number, batchSize: number): readonly number[] {
  const result: number[] = [];
  let remaining = totalRows;
  while (remaining > 0) {
    const size = Math.min(batchSize, remaining);
    result.push(size);
    remaining -= size;
  }
  return result;
}

function cyclicId(rows: readonly { readonly id: number }[], index: number): number {
  const row = rows[index % rows.length];
  if (row === undefined) throw new Error("The hot-write seed set is empty.");
  return row.id;
}

function extendedClient(
  prisma: PrismaClient,
  manifest: AtticManifest<PrismaClient>,
  redis: RedisLike,
  onEvent: (event: AtticEvent) => void,
) {
  return prisma.$extends(withAttic({ manifest, redis, worker: { embedded: false }, onEvent }));
}

async function run(): Promise<void> {
  const databaseUrl = process.env.DATABASE_URL;
  const redisUrl = process.env.REDIS_URL;
  if (databaseUrl === undefined || redisUrl === undefined) {
    throw new Error("The write benchmark requires DATABASE_URL and REDIS_URL.");
  }
  if (process.env.ATTIC_BENCH_CONFIRM !== "1") {
    throw new Error("Set ATTIC_BENCH_CONFIRM=1 to acknowledge that the benchmark creates database objects and load.");
  }

  const config = benchmarkConfig();
  const runId = randomUUID().replaceAll("-", "").slice(0, 20);
  const baselineSchema = `attic_bench_base_${runId}`;
  const atticSchema = `attic_bench_cache_${runId}`;
  const namespace = `attic-bench-${runId}`;
  const manifest = benchmarkManifest(atticSchema, namespace);
  const admin = new Pool({ connectionString: databaseUrl, max: config.concurrency + 4 });
  const redisClient = createClient({ url: redisUrl, disableOfflineQueue: true });
  redisClient.on("error", (error) => console.error("Redis client error during benchmark", error));
  const redis = new TrackingRedis(redisClient);
  const tracker = new PostCommitSyncTracker();
  let baseline: PrismaClient | undefined;
  let atticBase: PrismaClient | undefined;
  let attic: ReturnType<typeof extendedClient> | undefined;

  try {
    await admin.query(schemaSql(baselineSchema));
    await admin.query(schemaSql(atticSchema));
    await admin.query(renderMigrationSql(manifest));
    await redisClient.connect();

    baseline = new PrismaClient({
      adapter: new PrismaPg({ connectionString: databaseUrl }, { schema: baselineSchema }),
    });
    atticBase = new PrismaClient({
      adapter: new PrismaPg({ connectionString: databaseUrl }, { schema: atticSchema }),
    });
    attic = extendedClient(atticBase, manifest, redis, tracker.onEvent);
    const baselineClient = baseline;
    const atticClient = attic;
    await attic.$attic.start();

    for (let index = 0; index < config.warmupWrites; index += 1) {
      await baselineClient.user.create({ data: { email: `${runId}-baseline-warmup-${String(index)}@bench.test` } });
      await atticClient.user.create({ data: { email: `${runId}-attic-warmup-${String(index)}@bench.test` } });
    }
    tracker.reset();

    const results: BenchmarkResult[] = [];
    results.push(
      await measure(
        "sequential create",
        "PostgreSQL baseline",
        config.sequentialWrites,
        config.sequentialWrites,
        1,
        (index) =>
          baselineClient.user.create({ data: { email: `${runId}-baseline-sequential-${String(index)}@bench.test` } }),
      ),
    );
    const atticSequential = await measure(
      "sequential create",
      "Attic",
      config.sequentialWrites,
      config.sequentialWrites,
      1,
      (index) => atticClient.user.create({ data: { email: `${runId}-attic-sequential-${String(index)}@bench.test` } }),
    );
    const sequentialSync = tracker.take();
    results.push({
      ...atticSequential,
      ...(sequentialSync === undefined ? {} : { postCommitSync: sequentialSync }),
      outboxBacklog: (await attic.$attic.health()).outbox?.pendingEvents ?? 0,
    });

    const seedCount = Math.max(config.concurrency * 4, 32);
    const baselineSeeds = await baselineClient.user.createManyAndReturn({
      data: Array.from({ length: seedCount }, (_, index) => ({
        email: `${runId}-baseline-hot-${String(index)}@bench.test`,
      })),
      select: { id: true },
    });
    const atticSeeds = await atticClient.user.createManyAndReturn({
      data: Array.from({ length: seedCount }, (_, index) => ({
        email: `${runId}-attic-hot-${String(index)}@bench.test`,
      })),
      select: { id: true },
    });
    tracker.reset();

    results.push(
      await measure(
        "hot User updates",
        "PostgreSQL baseline",
        config.concurrentWrites,
        config.concurrentWrites,
        config.concurrency,
        (index) =>
          baselineClient.user.update({
            where: { id: cyclicId(baselineSeeds, index) },
            data: { name: `revision-${String(index)}` },
          }),
      ),
    );
    const atticConcurrent = await measure(
      "hot User updates",
      "Attic",
      config.concurrentWrites,
      config.concurrentWrites,
      config.concurrency,
      (index) =>
        atticClient.user.update({
          where: { id: cyclicId(atticSeeds, index) },
          data: { name: `revision-${String(index)}` },
        }),
    );
    const concurrentSync = tracker.take();
    results.push({
      ...atticConcurrent,
      ...(concurrentSync === undefined ? {} : { postCommitSync: concurrentSync }),
      outboxBacklog: (await attic.$attic.health()).outbox?.pendingEvents ?? 0,
    });

    const bulkBatches = batches(config.bulkRows, config.bulkBatchSize);
    let baselineBulkOffset = 0;
    results.push(
      await measure("createMany batches", "PostgreSQL baseline", bulkBatches.length, config.bulkRows, 1, (index) => {
        const size = bulkBatches[index] ?? 0;
        const offset = baselineBulkOffset;
        baselineBulkOffset += size;
        return baselineClient.user.createMany({
          data: Array.from({ length: size }, (_, row) => ({
            email: `${runId}-baseline-bulk-${String(offset + row)}@bench.test`,
          })),
        });
      }),
    );
    let atticBulkOffset = 0;
    const atticBulk = await measure("createMany batches", "Attic", bulkBatches.length, config.bulkRows, 1, (index) => {
      const size = bulkBatches[index] ?? 0;
      const offset = atticBulkOffset;
      atticBulkOffset += size;
      return atticClient.user.createMany({
        data: Array.from({ length: size }, (_, row) => ({
          email: `${runId}-attic-bulk-${String(offset + row)}@bench.test`,
        })),
      });
    });
    const bulkSync = tracker.take();
    results.push({
      ...atticBulk,
      ...(bulkSync === undefined ? {} : { postCommitSync: bulkSync }),
      outboxBacklog: (await attic.$attic.health()).outbox?.pendingEvents ?? 0,
    });

    const output = {
      runId,
      node: process.version,
      config,
      note: "Exploratory measurement only; no pass/fail threshold is applied.",
      results,
    };
    if (process.env.ATTIC_BENCH_OUTPUT === "json") {
      console.log(JSON.stringify(output, null, 2));
    } else {
      console.log(`Attic write benchmark ${runId}`);
      console.log(config);
      console.table(
        results.map((result) => ({
          scenario: result.scenario,
          implementation: result.implementation,
          opsPerSecond: result.operationsPerSecond,
          rowsPerSecond: result.rowsPerSecond,
          p50Ms: result.p50Ms,
          p95Ms: result.p95Ms,
          p99Ms: result.p99Ms,
          syncP95Ms: result.postCommitSync?.p95Ms ?? "n/a",
          backlog: result.outboxBacklog ?? "n/a",
        })),
      );
      console.log(JSON.stringify(output, null, 2));
    }
  } finally {
    await attic?.$attic.stop().catch(() => undefined);
    await baseline?.$disconnect().catch(() => undefined);
    await atticBase?.$disconnect().catch(() => undefined);
    await redis.cleanup().catch(() => undefined);
    if (redisClient.isOpen) await redisClient.quit().catch(() => undefined);

    await admin.query("DELETE FROM attic.outbox WHERE namespace = $1", [namespace]).catch(() => undefined);
    await admin.query("DELETE FROM attic.tag_state WHERE namespace = $1", [namespace]).catch(() => undefined);
    await admin.query("DELETE FROM attic.installation WHERE namespace = $1", [namespace]).catch(() => undefined);
    await admin.query(`DROP SCHEMA IF EXISTS ${quoteIdentifier(atticSchema)} CASCADE`).catch(() => undefined);
    await admin.query(`DROP SCHEMA IF EXISTS ${quoteIdentifier(baselineSchema)} CASCADE`).catch(() => undefined);
    await admin.end().catch(() => undefined);
  }
}

void run().catch((error: unknown) => {
  console.error(error);
  process.exitCode = 1;
});
